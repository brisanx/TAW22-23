package org.taw.gestorbanco.dto;

public class EmpleadoDTO {
    private String nombre;

    private String rol;

    public EmpleadoDTO() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }
}
